public class Forone1 {
    public static void main(String[] args) {
        for (int a=0 ; a<8 ; a++) {
            System.out.println(a);
        }


        System.out.println();
        for (int a=2,b=12; a<b; a++,b-- ) {
            System.out.println("a=" + a);
            System.out.println("b=" + b);
        }


    }
}

public class switchtest1 {
    public static void main(String[] args) {
        int a = 567;
        switch (a) {
            case 555:
                System.out.println("现在a的值是555");
                break;
            case 557:
                System.out.println("现在a的值是557");
                break;
            case 567:
                System.out.println("现在a的值是567");
                break;
            default:
                System.out.println("no");
        }
    }
}
